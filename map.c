void carteDuJeu (SDL_Renderer* ecran, SDL_Texture* sol,SDL_Texture* ciel , SDL_Texture* obstacle ,SDL_Texture* trou, int w, int h){


    SDL_Rect dest, pos ;
    dest.x = 0 ;
    dest.y = 0 ;
    
    //hauteur d'une case
    if(h % HAUTEUR_MAP==0){
        dest.h = (h / HAUTEUR_MAP) ; // h = hauteur_fenetre
    }
    else{
        dest.h = (h / HAUTEUR_MAP) + 1;
    }
    //largeur d'une case
    if(w % LARGEUR_MAP==0){
        dest.w = w / LARGEUR_MAP ; // w = largeur_fenetre
    }
    else{
        dest.w = (w / LARGEUR_MAP) + 1 ;
    }
    //printf(" dest : %d,%d \n" ,dest.h,dest.w);
    //printf(" h,w : %d,%d \n" ,h,w);
    pos.h = dest.h ;
    pos.w = dest.w ;

    for (int i = 0 ; i < LARGEUR_MAP ; i++){ 
        for (int j = 0 ; j < HAUTEUR_MAP ; j++) {
            pos.x = (i * dest.w) ;
            pos.y = (j * dest.h)  ;
            int champ = map[(i+j*LARGEUR_MAP) ]; 
            switch(champ) 
            {
                case 0 :
                SDL_RenderCopy(ecran, ciel, &dest, &pos) ;
                break ;
                case 1 :
                SDL_RenderCopy(ecran, trou, &dest, &pos) ;
                break ;
                case 2 :
                SDL_RenderCopy(ecran, obstacle, &dest, &pos) ;
                break ;
                case 3 :
                SDL_RenderCopy(ecran, sol, &dest, &pos) ;
                break ;
            }
        }
    }



}
 
