void carteDuJeu (SDL_Renderer* ecran, SDL_Texture* sol,SDL_Texture* ciel , SDL_Texture* obstacle ,SDL_Texture* trou){


    SDL_Rect dest, pos ;
    dest.x = 0 ;
    dest.y = 0 ;
    dest.h = 20 ;//modif
    dest.w = 20 ;//modif

    pos.h = dest.h ;
    pos.w = dest.w ;

    for (int i = 0 ; i < LARGEUR_FENETRE / dest.w ; i++){ 
        for (int j = 0 ; j < HAUTEUR_FENETRE / dest.h; j++) {
            pos.x = (i * dest.w) % SURFACE_MAP ;
            pos.y = (j * dest.h) % SURFACE_MAP ;
            int champ = map[(i+j*LARGEUR_MAP) % SURFACE_MAP]; 
            switch(champ) 
            {
                case 0 :
                SDL_RenderCopy(ecran, ciel, &dest, &pos) ;
                break ;
                case 1 :
                SDL_RenderCopy(ecran, trou, &dest, &pos) ;
                break ;
                case 2 :
                SDL_RenderCopy(ecran, obstacle, &dest, &pos) ;
                break ;
                case 3 :
                SDL_RenderCopy(ecran, sol, &dest, &pos) ;
                break ;
            }
        }
    }



}
 
