void carteDuJeu (SDL_Renderer* ecran, SDL_Texture* sol,SDL_Texture* ciel , SDL_Texture* obstacle ,SDL_Texture* trou){


    SDL_Rect dest, pos ;
    dest.x = 0 ;
    dest.y = 0 ;
    dest.h = 10 ;//modif
    dest.w = 10 ;//modif

    pos.h = dest.h ;
    pos.w = dest.w ;

    for (int i = 0 ; i < 400 / dest.w ; i++){ //modifier les valeur taille de la fenetre / dest.h/w
        for (int j = 0 ; j < 200 / dest.h; j++) {
            pos.x = i * dest.w ;
            pos.y = j * dest.h ;
            int champ = map[(i+j*40)%600]; // A CORRIGER !!!! 
            switch(champ) 
            {
                case 0 :
                SDL_RenderCopy(ecran, ciel, &dest, &pos) ;
                break ;
                case 1 :
                SDL_RenderCopy(ecran, trou, &dest, &pos) ;
                break ;
                case 2 :
                SDL_RenderCopy(ecran, obstacle, &dest, &pos) ;
                break ;
                case 3 :
                SDL_RenderCopy(ecran, sol, &dest, &pos) ;
                break ;
            }
        }
    }



}
 
